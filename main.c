void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  if (htim->Instance == TIM2)
  {
    if (HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1) > risingEdgeTime)
    {
      risingEdgeTime = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
    }
    else
    {
      fallingEdgeTime = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);

      // Calculate period and pulse width
      uint32_t period = fallingEdgeTime - risingEdgeTime;
      uint32_t pulseWidth = fallingEdgeTime - htim->Instance->CCR1;

      // Check for division by zero
      if (period != 0)
      {
        // Calculate duty cycle
        dutyCycle = ((float)pulseWidth / (float)period) * 100.0f;
      }
      else
      {
        // Handle the case where the period is zero (avoid division by zero)
        dutyCycle = 0.0f;
      }

      // Calculate frequency
      if (period != 0)
      {
        frequency = 1.0f / ((float)period / (float)htim->Init.Prescaler / (float)HAL_RCC_GetPCLK1Freq());
      }
      else
      {
        // Handle the case where the period is zero (avoid division by zero)
        frequency = 0.0f;
      }

      // Reset the edge times for the next cycle
      risingEdgeTime = 0;
      fallingEdgeTime = 0;
    }
  }
}

